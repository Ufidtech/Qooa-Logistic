let currentLang = "en";

// Initialize language system
function initLanguage() {
  const savedLang = localStorage.getItem("qooa_language") || "en";
  setLanguage(savedLang);

  const langToggle = document.getElementById("langToggle");
  if (langToggle) {
    langToggle.addEventListener("click", toggleLanguage);
  }
}

// Toggle between languages
function toggleLanguage() {
  const newLang = currentLang === "en" ? "pidgin" : "en";
  setLanguage(newLang);
}

// Set language and update UI
function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("qooa_language", lang);

  // Update current language display
  const currentLangEl = document.getElementById("currentLang");
  if (currentLangEl) {
    currentLangEl.textContent = lang === "en" ? "English" : "Pidgin";
  }

  // Update all elements with data-lang attribute
  document.querySelectorAll("[data-lang]").forEach((element) => {
    const key = element.getAttribute("data-lang");
    if (translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });

  // Update placeholders
  document.querySelectorAll("[data-lang-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-lang-placeholder");
    if (translations[lang][key]) {
      element.placeholder = translations[lang][key];
    }
  });

  // Update option texts
  document.querySelectorAll("option[data-lang]").forEach((option) => {
    const key = option.getAttribute("data-lang");
    if (translations[lang][key]) {
      option.textContent = translations[lang][key];
    }
  });
}

// Get translated text
function t(key) {
  return translations[currentLang][key] || key;
}

// Initialize on load
if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", initLanguage);
} else {
  initLanguage();
}
